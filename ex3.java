public class ex3 {
    public static void main(String[] args) {
        for (char ch = 'A'; ch <= 'Z'; ch++) {
            int ascii = ch;
            System.out.println(ch + " - " + ascii);
        }
        for (char ch = 'a'; ch <= 'z'; ch++) {
            int ascii = ch;
            System.out.println(ch + " - " + ascii);
        }
    }
}